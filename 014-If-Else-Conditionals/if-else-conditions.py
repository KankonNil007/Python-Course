# If Else Conditionals

a = int(input("Enter Your Age: "))
print("Your age is:", a)

if(a < 18) :
    print("You Can't Drive")
    print("No")
else:
    print("You Can Drive")
    print("Yes")
print("Yay!")

# Conditional Operators

# These are the operators : >,<,>=,<=,==,!=

print(a < 18)
print(a > 18)
print(a <= 18)
print(a >= 18)
print(a == 18)
print(a != 18)

